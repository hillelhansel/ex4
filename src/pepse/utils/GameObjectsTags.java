package pepse.utils;

/**
 * Defines the tags used to identify different types of game objects within the game.
 */
public enum GameObjectsTags {
    /**
     * Represents the avatar tag.
     */
    AVATAR,
    /**
     * Represents the ground tag.
     */
    GROUND,
    /**
     * Represents the trunk tag.
     */
    TRUNK,
    /**
     * Represents the leaf tag.
     */
    LEAF,
    /**
     * Represents the fruit tag.
     */
    FRUIT,
    /**
     * Represents the sun tag.
     */
    SUN,
    /**
     * Represents the night tag.
     */
    NIGHT,
    /**
     * Represents the sunhalo tag.
     */
    SUNHALO,
    /**
     * Represents the sky tag.
     */
    SKY,
    /**
     * Represents the energyUI tag.
     */
    ENERGYUI
}